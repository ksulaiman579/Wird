const String zakahGeneralNote =
    'Zakah is due on wealth held for one lunar year (hawl) that reaches the '
    'nisab threshold. Amounts here are estimates to help you plan — consult '
    'a trustworthy scholar for your specific situation.';

const String monetaryNote =
    'Cash, bank savings, money owed to you that you expect to recover, and '
    'business/trade goods are pooled together and zakated at 2.5% once the '
    'total (after debts due now) reaches nisab. Nisab is the value of 85g of '
    'gold or 595g of silver — silver gives the lower threshold and is the '
    'more cautious basis for the poor.';

const String metalsNote =
    'Gold and silver you own are valued at today\'s price per gram and added '
    'to your zakatable wealth at 2.5%. Enter the weight you hold; jewellery '
    'in regular personal use is treated differently across schools — check '
    'with a scholar.';

const String businessNote =
    'Trade goods (inventory held for sale) are valued at their current sale '
    'value on your zakah due-date and zakated at 2.5%, alongside cash. Fixed '
    'assets used to run the business (premises, tools) are not zakatable.';

const String investmentsNote =
    'Shares bought to trade (resale) are zakated on their full market value '
    'at 2.5%. Shares held long-term for dividends are zakated on the '
    'company\'s underlying zakatable assets — enter only that zakatable '
    'portion, not the whole share price.';

const String agricultureNote =
    'Crops and fruits that are stored and measured are due at harvest once '
    'they reach roughly 653 kg (five awsuq). The rate is 10% (ushr) when '
    'watered naturally by rain or rivers, and 5% when watered by costly '
    'means such as wells or pumps. Payable in kind from the produce.';

const String livestockNote =
    'Free-grazing (sa\'imah) camels, cattle and sheep/goats kept for a full '
    'year are zakated per fixed classical tables, and the zakah is paid in '
    'animals of a specified age-grade. Animals worked for labour or fed by '
    'purchased fodder follow different rulings.';

const String rikazNote =
