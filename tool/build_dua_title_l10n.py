def main():
    with open(SRC, encoding="utf-8") as f:
        cats = json.load(f)["categories"]

    out = {}
    missing = []
    for c in cats:
        cid = c["id"]  # hm-cat-N
        n = int(cid.replace("hm-cat-", ""))
        en = (c.get("titleEnglish") or "").strip()
        ar = AR.get(n, "").strip()
        if not en or not ar:
            missing.append(cid)
            continue
        out[cid] = {"en": en, "ar": ar}

    if missing:
        print("ERROR: missing en/ar for:", missing, file=sys.stderr)
        sys.exit(1)
    if len(out) != len(cats):
        print(f"ERROR: {len(out)} titles for {len(cats)} categories", file=sys.stderr)
        sys.exit(1)

    with open(OUT, "w", encoding="utf-8", newline="\n") as f:
        json.dump(out, f, ensure_ascii=False, indent=2)
        f.write("\n")
    print(f"Wrote {len(out)} localized titles to {OUT} (en + ar).")


if __name__ == "__main__":
    main()
