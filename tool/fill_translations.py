def main():
    total = 0
    for lang, entries in FILL.items():
        path = os.path.join(L10N, f"app_{lang}.arb")
        if not os.path.isfile(path):
            print(f"SKIP {lang}: no {path}")
            continue
        d = json.load(open(path, encoding="utf-8"))
        changed = 0
        for k, v in entries.items():
            if k in d and d[k] != v:
                d[k] = v
                changed += 1
            elif k not in d:
                d[k] = v
                changed += 1
        with open(path, "w", encoding="utf-8") as f:
            json.dump(d, f, ensure_ascii=False, indent=2)
            f.write("\n")
        total += changed
        print(f"{lang}: updated {changed} keys")
    print(f"\nTotal keys updated: {total}")


if __name__ == "__main__":
    main()
