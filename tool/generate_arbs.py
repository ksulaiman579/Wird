    os.makedirs(L10N_DIR, exist_ok=True)
    for code, data in TRANSLATIONS.items():
        arb_data = {"@@locale": code}
        arb_data.update(data)
        file_path = os.path.join(L10N_DIR, f"app_{code}.arb")
